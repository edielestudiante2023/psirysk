<h2>Condiciones Intralaborales</h2>

<p>
    Se entienden como aquellas características del trabajo y de su organización que influyen en la salud
    y bienestar de los colaboradores; el modelo en el que se basa la batería retoma elementos de los
    modelos de demanda-control-apoyo social del Karasek, Theorell (1990) y Jonhson, del modelo de
    desequilibrio esfuerzo-recompensa de Siegrist (1996 y 2008) y del modelo dinámico de los factores
    de riesgo psicosocial de Villalobos (2005) (Ministerio de Protección Social, 2010). Para esta condición
    el instrumento establece cuatro dominios que cada uno agrupa un conjunto dimensiones que
    actúan como posibles fuentes de riesgo permitiendo su identificación y valoración. Los dominios
    que corresponden al factor intralaboral son:
