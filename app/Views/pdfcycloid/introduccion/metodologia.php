<h3>Procedimiento</h3>

<ul>
    <li>Para iniciar el proceso de valoración del riesgo psicosocial se solicitó:</li>
    <li>Una entrevista con el responsable del Sistema de Gestión de Seguridad y Salud en el Trabajo, para conocer el alcance y planificación de la intervención.</li>
</ul>

