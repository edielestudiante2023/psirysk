<h2 class="section-title" style="color: #006699; text-align: center; text-decoration: underline; margin-top: 30px; margin-bottom: 20px;">Condiciones Individuales:</h2>

<p style="text-align: justify; line-height: 1.6;">
    Aluden una serie de características sociodemográficas de cada colaborador como lo son el sexo, la
    edad, el estado civil, el nivel educativo, la profesión u oficio, el lugar de residencia, el estrato
    socioeconómico, el tipo de vivienda y el número de dependientes. Estas características
    sociodemográficas pueden modular la percepción y el efecto de los factores de riesgo Intralaborales
    y Extralaborales. Al igual que las características sociodemográficas, existen otros aspectos
    ocupacionales de los colaboradores que también pueden modular los factores psicosociales tales
    como la antigüedad en la empresa, el cargo, el tipo de contratación y la modalidad de pago, entre
    otras, las cuales se indagan con los instrumentos de la batería para la evaluación de los factores
    psicosociales.
</p>

