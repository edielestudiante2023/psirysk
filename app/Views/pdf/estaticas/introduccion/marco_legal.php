<h2 class="section-title" style="color: #006699; text-align: center; margin-bottom: 20px;">Marco Legal</h2>

<p style="text-align: justify; line-height: 1.6;">
    A continuación, se relaciona algunas normatividades aplicables para la valoración en salud y factores
    de riesgo psicosocial en entornos laborales colombianos:
</p>

<ul style="list-style-type: disc; margin-left: 30px; line-height: 1.8;">
    <li style="margin-bottom: 15px; text-align: justify;">
        <strong>Decreto 614 de 1984</strong> en el literal c del artículo 2 señala como objeto de la salud ocupacional,
        proteger a la persona contra los riesgos relacionados con agentes físicos, químicos,
        biológicos, psicosociales, mecánicos, eléctricos y otros derivados de la organización laboral
        que puedan afectar la salud individual y colectiva en los lugares de trabajo.
    </li>
    <li style="margin-bottom: 15px; text-align: justify;">
        <strong>Resoluciones 1016 de 1989 y 1075 de 1992</strong> se emite que los empleadores públicos y
        privados, incluirán dentro de las actividades del subprograma de medicina preventiva,
        campañas específicas tendientes a fomentar la prevención y el control sustancias
        psicoactivas, alcoholismo y tabaquismo en sus colaboradores. Y en el numeral 12 del
        artículo 10, una de las actividades del mismo subprograma es diseñar y ejecutar actividades
        orientadas a la prevención y control de enfermedades generadas por los riesgos
        psicosociales.
    </li>
    <li style="margin-bottom: 15px; text-align: justify;">
        <strong>Ley 1010 de 2006</strong> por medio de la cual se adoptan medidas para prevenir el acoso laboral y
        otros hostigamientos en el marco de las relaciones de trabajo.
    </li>
    <li style="margin-bottom: 15px; text-align: justify;">
        <strong>Resolución 2646 de 2008</strong> por la cual se establecen las disposiciones y se definen
        responsabilidades para la identificación, evaluación, prevención, intervención y monitoreo
        permanente de la exposición a factores de riesgo psicosocial en el trabajo y para la
        determinación del origen de las patologías causadas por el estrés ocupacional.
    </li>
    <li style="margin-bottom: 15px; text-align: justify;">
        <strong>Resolución 652 de 2012</strong> en donde se establece la exigencia de la conformación y
        funcionamiento del Comité de Convivencia Laboral, el cual pretende prevenir la aparición
        de acoso laboral el cual se encuentra claramente definido en la Ley 1010 de 2006. Que el
        artículo 3 de la resolución 2646 del 2008, se establecen las disposiciones y se definen
        responsabilidades para la identificación, evaluación, prevención, intervención y monitoreo
        permanente de la exposición a factores de riesgo psicosocial en el trabajo y para la
        determinación del origen de las patologías causadas por el estrés ocupacional y, adopta la
        definición de Acoso Laboral así:
    </li>
</ul>

