    <h4>1. Servicio</h4>
    <p>psyrisk (operada por CYCLOID TALENT SAS, NIT 901.653.912, "psyrisk", "nosotros") es una plataforma SaaS para la aplicación de la Batería de Riesgo Psicosocial conforme a la Resolución 2764 de 2022 del Mintrabajo.</p>

    <h4>2. Cuenta de Tenant</h4>
    <p>Cada cuenta (tenant) corresponde a un psicólogo o consultora individual. El tenant es el <strong>responsable</strong> del tratamiento de los datos de sus trabajadores evaluados; psyrisk actúa como <strong>encargado</strong>.</p>

    <h4>3. Pago</h4>
    <p>Suscripción mensual prepagada. Los créditos se acreditan al confirmarse el pago. Sin reembolso por créditos no utilizados al final del periodo.</p>
    <p>Los precios pueden actualizarse con 30 días de aviso previo.</p>

    <h4>4. Trial</h4>
    <p>14 días con créditos del plan elegido. Al expirar, el tenant decide pagar o quedar suspendido.</p>

    <h4>5. Cancelación</h4>
    <p>Se puede cancelar en cualquier momento. Los datos se conservan por 5 años (vigilancia epidemiológica) y luego se anonimizan.</p>

    <h4>6. Confidencialidad</h4>
    <p>Cada tenant ve únicamente sus propios datos. psyrisk garantiza aislamiento técnico y administrativo entre tenants.</p>

    <h4>7. SLA</h4>
    <p>Disponibilidad objetivo: 99.5% mensual. Notificación de incidentes mayores en 4 horas hábiles.</p>

    <h4>8. Limitación de responsabilidad</h4>
    <p>psyrisk no se hace responsable de decisiones del tenant basadas en interpretaciones erróneas de los reportes. La responsabilidad legal frente a empleadores y trabajadores recae sobre el tenant.</p>

    <h4>9. Jurisdicción</h4>
    <p>Estos términos se rigen por la ley colombiana. Cualquier disputa se resuelve ante los jueces de Bogotá.</p>

