<p style="text-align: justify; line-height: 1.6;">
    <strong>4. Recompensas:</strong> Este término trata de la retribución que el colaborador obtiene a cambio de sus
    contribuciones o esfuerzos laborales. Este dominio comprende diversos tipos de retribución: a) la
    financiera o compensación económica; b) de estima o compensación psicológica, la cual comprende
    el reconocimiento del grupo social y el trato justo en el trabajo y de posibilidades de promoción y
    seguridad en el trabajo. Otras formas de retribución que se consideran en este dominio comprenden
    las posibilidades de educación, la satisfacción y la identificación con el trabajo y con la organización.
</p>

