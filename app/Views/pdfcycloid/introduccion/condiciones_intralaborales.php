<p>
    <strong>1. Demandas del trabajo:</strong> Se refieren a las exigencias que el trabajo impone al colaborador, éstas
    pueden ser de diversa naturaleza, como cognitivas o mentales, emocionales, de responsabilidad,
    del ambiente laboral y de jornada de trabajo.
</p>

<p>
    <strong>2. Control sobre el trabajo:</strong> Posibilidad que el trabajo ofrece al colaborador para influir y tomar
    decisiones sobre los diversos aspectos que intervienen en su realización. La iniciativa y autonomía;
    el uso y desarrollo de habilidades y conocimientos; la participación y manejo del cambio; la claridad
    del rol y; la capacitación son algunos de estos aspectos.
</p>

<p>
    <strong>3. Liderazgo y relaciones sociales en el trabajo:</strong> Por un lado, el liderazgo alude a un tipo particular
    de relación social que se establece entre los superiores jerárquicos y sus colaboradores y cuyas
    características influyen en la forma de trabajar y en el ambiente de relaciones de un área. Por otro
    lado, el concepto de relaciones sociales en el trabajo indica la interacción que se establece con otras
    personas en el contexto laboral y abarca aspectos funcionales de las interacciones tales como la
    retroalimentación del desempeño, el trabajo en equipo, el apoyo social y los aspectos emocionales,
    como la cohesión.
</p>

