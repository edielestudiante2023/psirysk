<!-- PÁGINA 2: Metodología (continuación) -->

<ul>
    <li>Se definió el plan de intervención y definición de fechas de toma de la encuesta, así mismo el plan de socialización y capacitación de:</li>
</ul>

<ol type="a">
    <li>¿Qué es el riesgo psicosocial?</li>
    <li>Normatividad legal Riesgo psicosocial</li>
    <li>Objetivo y metodología de la aplicación.</li>
</ol>

<ul>
    <li>Se solicitó por medio de un consentimiento informado, la autorización de los colaboradores para el procedimiento de evaluación del riesgo psicosocial y cumplimiento con lo establecido en la Ley 1090.</li>
</ul>

<p>
    Posterior a la aplicación, se registra los resultados a un sistema de información y se realiza un análisis
    generando una calificación de riesgo de acuerdo con la Tabla de niveles de riesgo, con esto se
    aprovechará los hallazgos para definir recomendaciones generales y específicas asociadas al control
    del riesgo psicosocial en la organización.
</p>

